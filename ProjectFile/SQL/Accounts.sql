DROP TABLE ACCOUNTS, TRANSACTIONS;
CREATE TABLE ACCOUNTS (ACCOUNT_NO INT PRIMARY KEY, HOLDER_NAME VARCHAR(50), BALANCE DECIMAL(10,2));
CREATE TABLE TRANSACTIONS (TRANS_ID INT PRIMARY KEY, ACCOUNT_NO INT, TYPE VARCHAR(10), AMOUNT DECIMAL(10,2), DATE DATE, FOREIGN KEY (ACCOUNT_NO) REFERENCES ACCOUNTS(ACCOUNT_NO));
